package model;

/**
 *
 * @author ADMIN
 */
public class Order {
    private int id;
    private int userID;
    private double priceTotal;

    public Order() {
    }

    public Order(int id, int userID, double priceTotal) {
        this.id = id;
        this.userID = userID;
        this.priceTotal = priceTotal;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public double getPriceTotal() {
        return priceTotal;
    }

    public void setPriceTotal(double priceTotal) {
        this.priceTotal = priceTotal;
    }
    
}
